export class Vector2 {
    readonly x: number;
    readonly y: number;

    constructor (x: number = 0, y: number = 0) {
        this.x = x;
        this.y = y;
    }
}

export class Vector3 {
    readonly x: number;
    readonly y: number;
    readonly z: number;

    constructor(x: number = 0, y: number = 0, z: number = 0) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}